export type MisskeyTheme = {
  id: string
  base: "dark" | "light"
  desc: string
  name: string
  props: Record<string, string>
  author: string
}

export type ThemeEntry = {
  theme: MisskeyTheme
  style: string
  mood: string
  tags: string[]
  preview: {
    bg: string
    panel: string
    fg: string
    accent: string
    line: string
    soft: string
  }
}

type ThemeRecipe = {
  id: string
  base: MisskeyTheme["base"]
  desc: string
  name: string
  style: string
  mood: string
  tags: string[]
  colors: {
    bg: string
    fg: string
    panel: string
    popup?: string
    deckBg: string
    divider: string
    accent: string
    cwBg: string
    cwFg: string
    cwHoverBg: string
    infoBg: string
    infoFg: string
    warn: string
    badge: string
    error: string
    success: string
    codeNumber: string
    codeString: string
    codeBoolean: string
    fgOnAccent: string
    fgOnWhite?: string
    buttonGradateA?: string
    buttonGradateB?: string
    scrollbarHandle: string
    scrollbarHandleHover: string
    infoWarnBg?: string
    infoWarnFg?: string
  }
}

