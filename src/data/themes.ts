        badge: colors.badge,
        error: colors.error,
        focus: ":alpha<0.3<@accent",
        navBg: "@panel",
        navFg: "@fg",
        panel: colors.panel,
        popup,
        accent: colors.accent,
        deckBg: colors.deckBg,
